    /**
     * 调用手机里的应用打开相应文件
     *
     * @param context
     * @param filePath
     */
    public static void openFileBySystem(Context context, String filePath) {
        if (TextUtils.isEmpty(filePath)) {
            Toast.makeText(context, "文件不存在", Toast.LENGTH_SHORT).show();
            return;
        }
        try {
            Intent intent = new Intent();
            File file = new File(filePath);
            Uri uri = FileProvider.getUriForFile(context, context.getPackageName() + FILE_PROVIDER_PATH, file);
            //设置标记
            intent.addFlags(Intent.FLAG_GRANT_READ_URI_PERMISSION);
            //动作，查看
            intent.setAction(Intent.ACTION_VIEW);
            //设置类型
            intent.setDataAndType(uri, getMIMEType(file));
            context.startActivity(intent);
        } catch (Exception e) {
            Toast.makeText(context, "未识别文件路径", Toast.LENGTH_SHORT).show();
        }
    }

    private static String getMIMEType(File file) {
        String type = "*/*";
        String fName = file.getName();
        //获取后缀名前的分隔符"."在fName中的位置。
        int dotIndex = fName.lastIndexOf(".");
        if (dotIndex < 0) {
            return type;
        }
        //获取文件的后缀名
        String fileType = fName.substring(dotIndex, fName.length()).toLowerCase();
        if (fileType == null || "".equals(fileType)) {
            return type;
        }
        //在MIME和文件类型的匹配表中找到对应的MIME类型。
        for (int i = 0; i < MIME_MapTable.length; i++) {
            if (fileType.equals(MIME_MapTable[i][0])) {
                type = MIME_MapTable[i][1];
            }

        }
        return type;
    }
